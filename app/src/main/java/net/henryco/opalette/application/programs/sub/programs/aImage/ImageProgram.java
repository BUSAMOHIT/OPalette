package net.henryco.opalette.application.programs.sub.programs.aImage;

import android.support.annotation.NonNull;
import android.support.annotation.Nullable;

import net.henryco.opalette.api.glES.camera.Camera2D;
import net.henryco.opalette.api.glES.render.OPallRenderable;
import net.henryco.opalette.api.glES.render.graphics.fbo.FrameBuffer;
import net.henryco.opalette.api.glES.render.graphics.fbo.OPallFBOCreator;
import net.henryco.opalette.api.glES.render.graphics.shaders.shapes.FilterMatrices;
import net.henryco.opalette.api.glES.render.graphics.shaders.shapes.Vignette;
import net.henryco.opalette.api.glES.render.graphics.shaders.textures.Texture;
import net.henryco.opalette.api.glES.render.graphics.shaders.textures.extend.ConvolveTexture;
import net.henryco.opalette.api.utils.GLESUtils;
import net.henryco.opalette.api.utils.requester.OPallRequester;
import net.henryco.opalette.api.utils.requester.Request;
import net.henryco.opalette.api.utils.views.OPallViewInjector;
import net.henryco.opalette.application.programs.sub.AppSubProgram;
import net.henryco.opalette.application.programs.sub.AppSubProtocol;
import net.henryco.opalette.application.proto.AppMainProto;

import javax.microedition.khronos.opengles.GL10;

/**
 * Created by HenryCo on 21/03/17.
 */

public class ImageProgram implements AppSubProgram<AppMainProto>, AppSubProtocol, VignetteControl.VignetteControlHolder {


	private final GLESUtils.Color bgColor = new GLESUtils.Color(GLESUtils.Color.TRANSPARENT);
	private long id = methods.genID(ImageProgram.class);
	private ProxyRenderData<ConvolveTexture> proxyRenderData = new ProxyRenderData<>();
	private FrameBuffer textureBuffer;
	private Vignette vignette;
	private Texture vignetteTexture;
	private Texture disableStatusTexture;

	private OPallRequester feedBackListener;
	private AppSubProgramHolder holder;

	private final float[] defDim = {0,0};


	private boolean pipeLineStatus;

	private boolean firstTime;

	@Override
	public void setProgramHolder(AppSubProgramHolder holder) {
		this.holder = holder;
	}

	@Override
	public void setFeedBackListener(OPallRequester feedBackListener) {
		this.feedBackListener = feedBackListener;
	}

	@Override
	public void acceptRequest(Request request) {
		request.openRequest(update_proxy_render_state, () -> proxyRenderData.setStateUpdated());
		request.openRequest(set_filters_enable, () -> proxyRenderData.getRenderData().setFilterEnable(true));
		request.openRequest(set_filters_disable, () -> proxyRenderData.getRenderData().setFilterEnable(false));
		request.openRequest(set_pipe_line_enable, () -> pipeLineStatus = true);
		request.openRequest(set_pipe_line_disable, () -> pipeLineStatus = false);
	}


	@Override
	public long getID() {
		return id;
	}

	@Override
	public void setID(long id) {
		this.id = id;
	}



	@Override
	public void create(@Nullable GL10 gl, int width, int height, AppMainProto context) {

		if (feedBackListener == null) throw new RuntimeException("FeedBackListener(OPallRequester) == NULL!");

		pipeLineStatus = true;
		firstTime = true;

		vignette = new Vignette(width, height).setActive(false).setPower(0);
		vignetteTexture = new Texture();
		vignetteTexture.setScreenDim(width, height);

		disableStatusTexture = new Texture();
		disableStatusTexture.setScreenDim(width, height);

		textureBuffer = OPallFBOCreator.FrameBuffer(width, height, false);

		proxyRenderData.setRenderData(new ConvolveTexture());
		proxyRenderData.getRenderData().setScreenDim(width, height);
		proxyRenderData.setStateUpdated().getRenderData().setFilterMatrix(FilterMatrices.m_sharpen1());
		proxyRenderData.getRenderData().setEffectScale(0);
		proxyRenderData.getRenderData().setScreenDim(width, height);

		OPallViewInjector.inject(context.getActivityContext(), new FilterSharpnessControl(proxyRenderData));


		OPallViewInjector.inject(context.getActivityContext(), new CanvasSizeControl(width, height, feedBackListener));
//		FIXME: 01/04/17 // TODO: 01/04/17 Actually disabled need fix


		OPallViewInjector.inject(context.getActivityContext(), new BackGroundControl(bgColor));
		OPallViewInjector.inject(context.getActivityContext(), new VignetteControl(vignette, proxyRenderData, this));
		OPallViewInjector.inject(context.getActivityContext(), new NoiseControl(proxyRenderData));

		defDim[0] = width;
		defDim[1] =height;
	}




	@Override
	public void onSurfaceChange(@Nullable GL10 gl, AppMainProto context, int width, int height) {
		proxyRenderData.setStateUpdated();
		vignette.setScreenDim(width, height);
	}



	@Override
	public void render(@Nullable GL10 gl10, AppMainProto context, Camera2D camera, int w, int h) {

		if (firstTime) { // lazy init
			OPallViewInjector.inject(context.getActivityContext(), new RotationControl(proxyRenderData));
			OPallViewInjector.inject(context.getActivityContext(), new TranslationControl(proxyRenderData));
			firstTime = false;
		}

		if (pipeLineStatus) {
			if (proxyRenderData.stateUpdated()) {
				camera.backTranslate(() -> {
					camera.translateXY(w - defDim[0], h - defDim[1]); // position correction while canvas size changed
					boolean e = proxyRenderData.getRenderData().isFilterEnable();
					feedBackListener.sendRequest(new Request(e ? set_filters_enable : set_filters_disable).destination(d -> d.except(id)));
					feedBackListener.sendRequest(new Request(update_proxy_render_state).destination(d -> d.except(id)));


					if (vignette.isActive()) {
						vignette.render(camera);

						if (vignetteMode == VIGNETTE_IMAGE) {
							vignetteTexture.set(proxyRenderData.getRenderData());
							vignetteTexture.setTextureDataHandle(vignette.getShapeBuffer().getTextureBufferHandle());
						}
					}

					textureBuffer.beginFBO(() -> {

						proxyRenderData.getRenderData().render(camera, program -> GLESUtils.clear());

						if (vignette.isActive()) {
							if (vignetteMode == VIGNETTE_IMAGE) vignetteTexture.render(camera);
							else if (vignetteMode == VIGNETTE_SCREEN) {
								vignette.getShapeBuffer().render(camera);
							}
						}
					});


				});
			}
			finalBackGroundData.render(camera);
		} else {
			finalBackGroundData.render(camera);
			disableStatusTexture.set(proxyRenderData.getRenderData());
			disableStatusTexture.render(camera);
		}


	}


	private OPallRenderable finalBackGroundData = new OPallRenderable() {
		@Override
		public void render(Camera2D camera) {
			camera.backTranslate(() -> {
				if (!bgColor.equals(GLESUtils.Color.TRANSPARENT)) GLESUtils.clear(bgColor);
			});
		}
		@Override public void setScreenDim(float w, float h) {

		}
		@Override public int getWidth() {
			return 0;
		}
		@Override public int getHeight() {
			return 0;
		}
	};


	private int vignetteMode = VIGNETTE_IMAGE;
	public void setVignetteImage() {
		this.vignetteMode = VIGNETTE_IMAGE;
	}
	public void setVignetteScreen() {
		this.vignetteMode = VIGNETTE_SCREEN;
	}

	@Override
	public void setRenderData(OPallRenderable data) {
		if (data != null && data instanceof Texture) {
			proxyRenderData.getRenderData().set((Texture) data);
			proxyRenderData.setStateUpdated();
		}
	}

	@NonNull
	@Override
	public OPallRenderable getRenderData() {
		return textureBuffer.getTexture();
	}


	@Nullable @Override
	public OPallRenderable getFinalRenderData() {
		return finalBackGroundData;
	}
}
